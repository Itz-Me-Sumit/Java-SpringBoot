public class Datatype {
    public static void main(String[] args){
        // Integers ---> Bytes , short , int , long
        byte b = 5;
        short s = 120 ;
        int i = 1202020 ;
        long l = 1000000000 ; 
        System.out.println("Integer Values ---> " + b +" " + s + " " + " " + i + " " + l);

        // Real Numbers ---> double , float
        double d = 23.23;
        float f = 23.23f;
        System.out.println("Real Numbers ---> " + d + " " + f);

        // Charcters
        char c = 'A';
        System.out.println("Char ---> " + c);

        // Boolean ---> true , false
        boolean bool = false;
        System.out.println("Boolean ---> " + bool);

    }
}
